export function getJokesByLanguage(language: "english" | "hinglish" | "mix"): DadJoke[] {
  if (language === "mix") return jokes;
  return jokes.filter((j) => j.language === language);
}

export function getDailyJoke(): DadJoke {
  const today = new Date();
  const dayOfYear = Math.floor(
    (today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / 86400000
  );
  return jokes[dayOfYear % jokes.length];
}

export function getJokeById(id: string): DadJoke | undefined {
  return jokes.find((j) => j.id === id);
}

export function getShuffledJokes(
  language: "english" | "hinglish" | "mix",
  exclude: string[] = [],
  seed?: number
): DadJoke[] {
  const all = getJokesByLanguage(language).filter((j) => !exclude.includes(j.id));
  const featured = all.filter((j) => j.featured);
  const rest = all.filter((j) => !j.featured);
  // Shuffle non-featured pool
  const rng = seed !== undefined ? seededRandom(seed) : Math.random;
  for (let i = rest.length - 1; i > 0; i--) {
    const j = Math.floor(rng() * (i + 1));
    [rest[i], rest[j]] = [rest[j], rest[i]];
  }
  // Featured always at the top
  return [...featured, ...rest];
}

function seededRandom(seed: number) {
  let s = seed;
  return () => {
    s = (s * 16807) % 2147483647;
    return (s - 1) / 2147483646;
  };
}
